A = int(input("Введите первую переменную - "))
B = int(input("Введите вторую переменную - "))

print("Снизу будет целая часть от деления первой переменной на вторую")
deli = A // B
print(round(deli))
print("Снизу будет целая и дробная часть от деления первой перемнной на вторую")
deli_sv = A / B
print(deli_sv)